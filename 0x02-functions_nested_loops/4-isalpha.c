/**
 * _isalpha - checks for aphabetic character
 * @c: variable for _islower
 * Return: if c is lowercase or uppercase return 0 otherwise
 */
int _isalpha(int c)
{
	if ((c > 'a' && c < 'z') || (c > 'A' && c < 'Z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}






	return (0);
}
